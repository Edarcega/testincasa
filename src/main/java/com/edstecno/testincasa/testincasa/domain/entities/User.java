package com.edstecno.testincasa.testincasa.domain.entities;

import org.springframework.data.annotation.Id;
import org.springframework.data.mongodb.core.mapping.Document;

import java.io.Serializable;
import java.util.ArrayList;
import java.util.List;
import java.util.Objects;

@Document(collation = "user")
public class User implements Serializable {


    private static final long serialVersionUID = -1604146369534819349L;
    @Id
    private Long id;
    private String nome;
    private String email;
    private List<Perfil> perfil = new ArrayList<>();
    private List<Endereco> enderecos = new ArrayList<>();
    private List<Personalidade> personalidades = new ArrayList<>();

    public User() {
    }


    public User(Long id, String nome, String email) {
        this.id = id;
        this.nome = nome;
        this.email = email;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        User user = (User) o;
        return Objects.equals(id, user.id);
    }

    @Override
    public int hashCode() {
        return Objects.hashCode(id);
    }
}
